#ifndef MATH_H
#define MATH_H

#include <cmath>

namespace stratos::utils {

template <typename T>
T round(const T value, const int precision) {
    const T factor = std::pow(10, precision);
    return std::round(value * factor) / factor;
}

} // namespace stratos::utils

#endif //MATH_H