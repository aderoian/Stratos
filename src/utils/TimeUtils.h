#ifndef TIME_H_
#define TIME_H_

#include <chrono>

namespace stratos::utils {

inline long currentTimeMillis() {
    return std::chrono::duration_cast<std::chrono::milliseconds>(std::chrono::system_clock::now().time_since_epoch()).count();
}

} // namespace stratos::utils

#endif //TIME_H_
